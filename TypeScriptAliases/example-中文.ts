// 中文類型別名使用範例
import { 文本, 整數, 布林值, 列表, 可選, 非同步操作 } from './中文類型別名';

// 基本使用
const 姓名: 文本 = "張三";
const 年齡: 整數 = 30;
const 是否啟用: 布林值 = true;

// 集合使用
const 愛好: 列表<文本> = ["閱讀", "編程", "遊戲"];
const 分數: 列表<整數> = [95, 87, 92];

// 可選值
const 中間名: 可選<文本> = null;
const 電話號碼: 可選<文本> = "555-1234";

// 使用類型別名的函數
function 問候(人名: 文本, 人齡: 整數): 文本 {
    return `你好，${人名}！你今年 ${人齡} 歲。`;
}

// 非同步函數範例
async function 獲取用戶數據(用戶ID: 整數): 非同步操作<文本> {
    // 模擬非同步操作
    return `用戶ID ${用戶ID} 的數據`;
}

// 使用函數
console.log(問候(姓名, 年齡));

獲取用戶數據(123).then((數據: 文本) => {
    console.log(數據);
});

// 導出供其他文件使用
export { 姓名, 年齡, 是否啟用, 愛好 };
